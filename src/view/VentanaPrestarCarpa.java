    private void btnPrestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPrestarActionPerformed
        // TODO add your handling code here:
        if(txfNombreP.getText().isEmpty() || txfIdP.getText().isEmpty() || txfTelefonoP.getText().isEmpty() || txfClave.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Hay campos vacios", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String nombre = txfNombreP.getText();
        String id = txfIdP.getText();
        String telefono = txfTelefonoP.getText();
        String palabraClave = txfClave.getText();
        Persona personaP = new Persona(id, nombre, telefono);
        LSE<Persona> personas = new LSE<>();
        personas.add(personaP);
        Carpa carpa = new Carpa(Integer.parseInt(txfNumCarpa.getText()), personas, palabraClave);
        controlador.prestarCarpa(carpa);
        JOptionPane.showMessageDialog(this, "Carpa prestada");
        volver();
    }//GEN-LAST:event_btnPrestarActionPerformed

