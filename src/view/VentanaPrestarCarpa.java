    private void txfIdPKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txfIdPKeyTyped
        // TODO add your handling code here:
        int key = evt.getKeyChar();
        boolean numeros = key>= 48 && key<=57;
        if(!numeros){
            evt.consume();
        }
    }//GEN-LAST:event_txfIdPKeyTyped

